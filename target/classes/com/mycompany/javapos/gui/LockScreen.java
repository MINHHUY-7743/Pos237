    private void jbtnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEnterActionPerformed
        String password = jPasswordField1.getText();

        // Kiểm tra mật khẩu
        if ("12345".equals(password)) {
            // Nếu đúng, chuyển sang màn hình khác
            POS posScreen = new POS();
            posScreen.setVisible(true);
            this.dispose();
        } else {
            // Nếu sai, hiển thị thông báo lỗi và xóa nội dung
            javax.swing.JOptionPane.showMessageDialog(this, "Sai mật khẩu, vui lòng thử lại!");
            jPasswordField1.setText(""); // Xóa nội dung của jtxtPassWord
        }
    }//GEN-LAST:event_jbtnEnterActionPerformed

