    private void handleNumberInput(String number) {
        String currentText = jPasswordField1.getText();

        // Kiểm tra nếu ô nhập trống
        if (currentText.isEmpty()) {
            jPasswordField1.setText(number);
        } else {
            // Nối số mới vào chuỗi hiện tại
            currentText += number;
            jPasswordField1.setText(currentText);
        }
    }
